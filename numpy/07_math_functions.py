import numpy as np

array = np.array([1, 2, 3, 4, 5])

print("sqrt: ", np.sqrt(array))
print("cos: ", np.exp(array))
print("tan: ", np.log(array))
print("sin: ",np.sin(array))
print("cos: ",np.cos(array))
print("pi: ",np.pi)

print("\n"+"="*20)

